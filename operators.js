// logical operator 

a=30;
 
console.log(`5<a<20 ${5<a && a<20}`) ;/* And -> t && f = T */
console.log(`5<a<20 ${5<a || a<20}`) ;/* or -> t or f - t */
console.log(`5<a<20 ${!(5<a && a<20)}`) /* not -> if its false it will return true */

